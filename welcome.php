The producer-company concept is aimed to combine the efficiency of a company with the 'spirit' of traditional cooperatives.

Producer companies aim to integrate smallholders into modern supply networks minimizing transaction and coordination costs, while benefiting from economies of scale.

The FPC will take over the responsibility of selling the produce at consumer’s doorsteps.FPC has shareholders also referred to as a stockholder, is any person, company, or institution that owns at least one share of a company’s stock.

As equity owners, shareholders are subject to capital gains (or losses) and/or dividend payments as residual claimants on a firm's profits.Shareholders also enjoy certain rights such as voting at shareholder meetings to approve things like board of directors’ members, dividend distributions, or mergers.

Farmer producer company were proposed as a way forward to get some form of land consolidation – and an integration of smallholders within an agricultural value chain – which is critical to tackle the problem rural agrarian crisis. Such a consolidation, together with appropriate training and skill development of rural youth for emerging farm and non-farm jobs, would appear to be the key to lifting one billion poor people out of poverty worldwide.

 In some countries like China, India, Indonesia and Vietnam, smallholder farmers have already begun to come together and form Farmer Producer Companies (FPCs). 

Farmer Producer Company is a company registered under the Companies Act, 2013, which has the objective of production, harvesting, procurement, grading, pooling, handling, marketing, selling, and export of primary produce of the Members or import of goods or services for their benefit. 

Produce are things that have been produced or grown, especially by farming. Therefore, a Producer Company deals primarily with agriculture and post-harvest processing activities.

FPCs enable their members to access financial and other inputs and services, including appropriate technologies for farming. The FPCs also organize collection, processing, storage and marketing of their members’ produce in high-value markets at an optimal price. 

These actions by the FPCs have thus reduced transaction costs and allowed the FPCs to enter into a partnership with private and public sector companies for purposes of supplying farm produce on more equal terms.

